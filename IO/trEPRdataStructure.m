dataStructure.data = [];
dataStructure.axes = struct(...
    'x',struct(...
        'values',[], ...
        'measure','', ...
        'unit','' ...
        ), ...
    'y',struct(...
        'values',[], ...
        'calibratedValues',[], ...
        'measure','', ...
        'unit','' ...
        ), ...
    'z',struct(...
        'values',[], ...
        'measure','', ...
        'unit','' ...
        ) ...
    );
