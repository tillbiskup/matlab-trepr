    'runs',[],...
    'operator','',...
    'field',struct(...
        'start',[],...
        'stop',[],...
        'step',[],...
        'unit','',...
        'calibration',struct(...
            'values',[],...
            'unit','',...
            'model',''...
            )...
        ),...
    'recorder',struct(...
        'sensitivity',struct(...
            'value',[],...
            'unit',''...
            ),...
        'averages',[],...
        'timeBase',struct(...
            'value',[],...
            'unit',''...
            ),...
        'bandwidth',struct(...
            'value',[],...
            'unit',''...
            ),...
        'coupling','',...
        'impedance',struct(...
            'value',[],...
            'unit',''...
            ),...
        'model',''...
        ),...
    'transient',struct(...
        'points',[],...
        'triggerPosition',[],...
        'length',[],...
        'unit',''...
        ),...
    'bridge',struct(...
        'MWfrequency',struct(...
            'value',[],...
            'unit',''...
            ),...
        'attenuation',struct(...
            'value',[],...
            'unit',''...
            ),...
        'bandwidth',struct(...
            'value',[],...
            'unit',''...
            ),...
        'amplification',struct(...
            'value',[],...
            'unit',''...
            ),...
        'calibration',struct(...
            'values',[],...
            'unit','',...
            'model',''...
            ),...
        'power',struct(...
            'value',[],...
            'unit',''...
            ),...
        'model',''...
        ),...
    'laser',struct(...
        'wavelength',struct(...
            'value',[],...
            'unit',''...
            ),...
        'repetitionRate',struct(...
            'value',[],...
            'unit',''...
            ),...
        'power',struct(...
            'value',[],...
            'unit',''...
            ),...
        'model','',...
        'opoDye',''...
        ),...
    'temperature',struct(...
        'value',[],...
        'unit',''...
        )...
    );
dataStructure.header = cell(0);
dataStructure.info = struct();
dataStructure.file = struct(...
    'name','', ...
    'type','' ...
    );
dataStructure.version = '1.1';


