            fieldValue = strrep(fieldValue,',','.');
        end
        
        % If value is in matching cell array
        [matches,index] = max(strcmp(value,matching(:,1)));
        if matches
            % Check whether there is some function to apply first
            if ~isempty(matching{index,3})
