function getCharacteristicsEntriesNumbers(dataset)
    try
        % Get appdata from info GUI
        mainWindow = trEPRguiGetWindowHandle(mfilename);
        ad = getappdata(mainWindow);
        
        types = fieldnames(dataset.characteristics);
        for type = 1:length(types)
            numberOfEntries = ...
                length(dataset.characteristics.(types{type}));
            if any(strcmpi(types{type},{'soi','poi','doi'})) && ...
                    numberOfEntries < 2 && ...
                    isempty(dataset.characteristics.(types{type}...
                    ).parameters.coordinates)
                numberOfEntries = 0;
            elseif any(strcmpi(types{type},'voi')) && ...
                    numberOfEntries < 2 && ...
                    ~dataset.characteristics.(types{type}).parameters.reduction.x
                numberOfEntries = 0;
            end
            ad.info.characteristics.(types{type}).count = numberOfEntries;
        end
        setappdata(mainWindow,'info',ad.info);
    catch exception
        trEPRexceptionHandling(exception)
    end
end

