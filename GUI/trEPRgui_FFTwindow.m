                    set(gh.backgroundfit_subtract_togglebutton,'Value',0);
                    ad.fft.bgfit.mode = 'bgfit';
                    % Set fit function
                    functions = ...
                        cellstr(get(gh.backgroundfit_popupmenu,'String'));
