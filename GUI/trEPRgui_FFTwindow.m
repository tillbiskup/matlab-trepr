                updateSliderPanel()
                
                % Update results panel dimension panel
                updateDimensionPanel('Correction');
                
                return;
            case 'Discard'
                ad.acc.data = [];
                setappdata(mainWindow,'acc',ad.acc);
                
                set(gh.summary_panel_edit,'String',...
                    'Accumulated data discarded');
                
                % Update main axes
